package railway.information.system.dao;

/**
 *
 * @author Oleksander
 */
public class Carriage {
    
    String carriageId;
    String carriageMark;
    String rollingStock;
    String carriageParentType;
    String carriageType;

    public String getCarriageId() {
        return carriageId;
    }

    public void setCarriageId(String carriageId) {
        this.carriageId = carriageId;
    }

    public String getCarriageMark() {
        return carriageMark;
    }

    public void setCarriageMark(String carriageMark) {
        this.carriageMark = carriageMark;
    }

    public String getRollingStock() {
        return rollingStock;
    }

    public void setRollingStock(String rollingStock) {
        this.rollingStock = rollingStock;
    }

    public String getCarriageParentType() {
        return carriageParentType;
    }

    public void setCarriageParentType(String carriageParentType) {
        this.carriageParentType = carriageParentType;
    }

    public String getCarriageType() {
        return carriageType;
    }

    public void setCarriageType(String carriageType) {
        this.carriageType = carriageType;
    }


   
}
