package railway.information.system.dao;

/**
 *
 * @author Oleksander
 */
public class Locomotive {
    String locoId;
    String locoMark;
    String rollingStock;
    String railRoadType;
    String locoType;
    String numberOfCarriages;

    public String getLocoId() {
        return locoId;
    }

    public void setLocoId(String locoId) {
        this.locoId = locoId;
    }

    public String getLocoMark() {
        return locoMark;
    }

    public void setLocoMark(String locoMark) {
        this.locoMark = locoMark;
    }

    public String getRollingStock() {
        return rollingStock;
    }

    public void setRollingStock(String rollingStock) {
        this.rollingStock = rollingStock;
    }

    public String getRailRoadType() {
        return railRoadType;
    }

    public void setRailRoadType(String railRoadType) {
        this.railRoadType = railRoadType;
    }

    public String getLocoType() {
        return locoType;
    }

    public void setLocoType(String locoType) {
        this.locoType = locoType;
    }

    public String getNumberOfCarriages() {
        return numberOfCarriages;
    }

    public void setNumberOfCarriages(String numberOfCarriages) {
        this.numberOfCarriages = numberOfCarriages;
    }
       
    
}
